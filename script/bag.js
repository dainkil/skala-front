/* ==========================================================================
   bag.js  —  가방 속 물품 목록 보여주기
   * myBag 배열에 소지품 객체(소지품 명 name, 소지품 수 count)를 담는다.
   * 반복문으로 배열을 돌며 각 소지품 객체의 내용을 한 줄씩 만들어 출력한다.
   * index.html 의 분홍색(Bag) 카드를 누르면 showMyBag() 이 실행된다.
   ========================================================================== */

function showMyBag() {
  // 1) 가방 속 소지품을 객체(소지품 명 + 소지품 수)로 만들어 배열에 담는다
  var myBag = [
    { name: "지갑",   count: 1 },
    { name: "이어폰", count: 2 },
    { name: "노트북", count: 1 },
    { name: "볼펜",   count: 3 },
    { name: "텀블러", count: 1 }
  ];

  // 2) 화면(alert)에 보여줄 내용을 담을 문자열을 제목으로 시작한다
  var result = "내 가방 속 물품 목록\n\n";

  // 3) 반복문으로 배열을 돌며 소지품 객체를 하나씩 꺼내 출력한다
  for (var i = 0; i < myBag.length; i++) {
    var item = myBag[i];               // i 번째 소지품 객체

    // 개발자 도구 콘솔에는 객체를 그대로 출력 (구조 확인용)
    console.log(item);

    // 소지품 명과 수를 "번호. 이름 : 개수개" 한 줄로 이어붙인다
    result += (i + 1) + ". " + item.name + " : " + item.count + "개\n";
  }

  // 4) 완성된 목록을 alert 창으로 한 번에 보여준다
  alert(result);
}
